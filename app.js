// capturamos el btn  por su id
const btn = document.getElementById('btn');
// capturamos el span mediante su clase.
const color = document.querySelector('.color');
// inciamos la captura de nuestro evento
btn.addEventListener('click',function () {
    // creamos una funcion para cambiar el color del body, capturando un click del btn
    const randNumber = getRandNumber(colors); 
    let colorRand;
    // cambio la propiedad del backgroundColor del body, pasandole un numero ramdon
    colorRand = document.body.style.backgroundColor = colors[randNumber];
    // cambio la propiedad de un texto
    color.textContent = colorRand;
})
function getRandNumber(arr) {
    // funcion para tener un numero a la random redodenado, se multiplica por la candidad de tama√±o del array para obtener un numero entre 0-4 
    return Math.floor(Math.random()*arr.length);
    
}